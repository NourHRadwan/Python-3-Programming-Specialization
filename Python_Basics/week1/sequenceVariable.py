# Sequence Variables type
# 1. string (class str)
a = 'Nour'
print(type(a))

# 2. list
example_List = [1 , 'hello' , 4.5]
print(type(example_List))

# 3. Tuples
example_tuples = (1 , 4.5 , 'hello')
print(type(example_tuples))

# 4. Dictionary
ed = {'a': 22 , 'b':44.4}
print(type(ed))
print(ed['a'])
print(ed['b'])

#5.boolean
print(type(True))
print(type(False))

#6. set
example_set = {1 , 'hello', 4.5}
print(type(example_set))